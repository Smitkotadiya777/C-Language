#include<stdio.h>

int main(){
	
	//sum//
	
	int a=10;
	int b=10;
	
	int sum=a+b;
	
	printf("%d\n",sum);
	
	//subtraction//
	
	int c=20;
	int d=10;
	
	int sub=c-d;
	
	printf("%d\n",sub);
	
	
	//multiplication//
	
	int e=10;
	int f=10;
	
	int multi=e*f;
	
	printf("%d\n",multi);
	
	//division//
	
	int s=100;
	int m=10;
	
	int divi=s/m;
	
	printf("%d\n",divi);
	
	
	//percent//
	
	int x=100;
	int y=10;
	
	int percent=x%y;
	
	printf("%d",percent);
	
	
	
	return 0;
}
